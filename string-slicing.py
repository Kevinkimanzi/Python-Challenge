# Slicing  = create a substring by extraction element from another string
# indexing[] or slicing()
# [start:stop:steps]

website = "http://google.com"

website2 = "http://github.com"

name = "Kevin Kimanzi"
first_name = name[0:5]

last_name = name[6:13]

print(first_name)
print(last_name)

slice = slice(7,-4)
print(website[slice])
print(website2[slice])





